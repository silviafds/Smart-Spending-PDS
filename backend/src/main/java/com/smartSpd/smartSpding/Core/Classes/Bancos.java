package com.smartSpd.smartSpding.Core.Classes;

public class Bancos {

    private int id;
    private String nome;

    public Bancos(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }
}
